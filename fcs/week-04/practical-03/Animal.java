class Animal{
	//attributes
	private String name;
	private double weight;
	private String favouriteFood;
	private int age;

	//basic constructor
	Animal(){}

	//parametric constructor
	Animal(String a, double b, String c, int d){
		this.name = a;
		this.weight = b;
		this.favouriteFood = c;
		this.age = d;
	}

	//accessors
	public String getName(){
		return this.name;
	}
	public double getWeight(){
		return this.weight;
	}
	public String getFavouritefood(){
		return this.favouriteFood;
	}
	public int getAge(){
		return this.age;
	}

	//mutators
	public void setName(String a){
		this.name = a;
	}
	public void setWeight(double b){
		this.weight = b;
	}
	public void setFavouritefood(String c){
		this.favouriteFood = c;
	}
	public void setAge(int d){
		this.age = d;
	}
}