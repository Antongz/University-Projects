class Book{
	//attributes
	private String title;
	private int year;
	private String publish;
	private int pages;

	//basic constructor
	Book(){}

	//parametric constructor
	Book(String a, int b, String c, int d){
		this.title = a;
		this.year = b;
		this.publish = c;
		this.pages = d;
	}

	//accessors
	public String getTitle(){
		return this.title;
	}
	public int getYear(){
		return this.year;
	}
	public String getPublish(){
		return this.publish;
	}
	public int getPages(){
		return this.pages;
	}

	//mutators
	public void setTitle(String a){
		this.title = a;
	}
	public void setYear(int b){
		this.year = b;
	}
	public void setPublish(String c){
		this.publish = c;
	}
	public void setPages(int d){
		this.pages = d;
	}
}