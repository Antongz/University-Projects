public class Node{
	//attributes
	private int index;//the index of this node

	//constructors
	Node(){
		this.index = -1;
	}

	Node(int index){
		this.index = index;
	}

	//accessors and mutators
	public int getIndex(){
		return this.index;
	}
	public void setIndex(int index){
		this.index = index;
	}

	//Methods:
	// Print the data (the index) saved in the node. 
	public void printNode(){
		System.out.println("\""+"Node <"+this.index+">"+"\"");
	}
}